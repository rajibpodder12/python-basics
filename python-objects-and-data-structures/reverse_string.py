mystring = input('Enter any string which is to be reversed:')

print(f'Your input is {mystring}.')
print(f'character extract: {mystring[3:]}') #Last index position is exclusive
print(f'print character from 2-> index position 3: {mystring[1:2]}')
print(f'Last Character: {mystring[-1:]}')
print(f'Fisrt three character: {mystring[:3]}')
print(f'every 2nd character: {mystring[::2]}')
print(f'reversing the string: {mystring[::-1]}')